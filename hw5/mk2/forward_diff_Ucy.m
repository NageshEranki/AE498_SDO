function fd = forward_diff_Ucy(r0)
    
	%--------------------------------------------------------------------------
	%   Material constants (in SI units)
	%--------------------------------------------------------------------------
	E = 75e6;   nu = 0.3;   t = 0.1; p = 3;
	P0 = -100e3;
	%--------------------------------------------------------------------------
	%   Mesh params
	%--------------------------------------------------------------------------
	L = 6;  H = 2;
	Nx = 60;   Ny = 20;
    %--------------------------------------------------------------------------
    assert( (length(r0) == Nx*Ny) , "Length of r0 doesnt correspond to 60x20!")
	%--------------------------------------------------------------------------
	%   Step-size
	h = 1e-7;
	%--------------------------------------------------------------------------
	%	Sensitivity formula
	%--------------------------------------------------------------------------
	fd = zeros(Nx*Ny,1);
	%--------------------------------------------------------------------------
	edof = list_dofs(1:Nx*Ny, Nx);
    d = fem_hw5( L,H,Nx,Ny,edof,t,r0,p,E,nu,P0 );
    %   DOUBLE CHECK!
    d0 = d(end);
    for i = 1:Nx*Ny
	    tic
	    r = r0;
	    r(i) = r(i)+h;
	    d = fem_hw5( L , H , Nx , Ny , edof , t , r , p , E , nu , P0 );
	    %   DOUBLE CHECK!
	    di = d(end);
	    fd(i) = (di-d0)/h;
    end
end

